public class pattern4 {
    public static void main(String[] args) {
        /* ****
           ****
           ****
           ****

           this is the pattern that we are going to print using for loop
         */

         for(int i=1;i<=4;i++){
            for(int j=1;j<=4;j++){
                System.out.print("*");
            }
            System.out.println(" ");
         }
    }
}
